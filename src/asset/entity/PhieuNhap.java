package asset.entity;

import java.util.Date;

public class PhieuNhap {
	private String maPN;
	private Date ngayLap;
	private String lyDo;
	private String taiKhoanChinh;
	private int thueGTGT;
	private String maNV;
	private String maKH;
	private String maKho;

	public String getMaPN() {
		return maPN;
	}

	public void setMaPN(String maPN) {
		this.maPN = maPN;
	}

	public Date getNgayLap() {
		return ngayLap;
	}

	public void setNgayLap(Date ngayLap) {
		this.ngayLap = ngayLap;
	}

	public String getLyDo() {
		return lyDo;
	}

	public void setLyDo(String lyDo) {
		this.lyDo = lyDo;
	}

	public String getTaiKhoanChinh() {
		return taiKhoanChinh;
	}

	public void setTaiKhoanChinh(String taiKhoanChinh) {
		this.taiKhoanChinh = taiKhoanChinh;
	}

	public int getThueGTGT() {
		return thueGTGT;
	}

	public void setThueGTGT(int thueGTGT) {
		this.thueGTGT = thueGTGT;
	}

	public String getMaNV() {
		return maNV;
	}

	public void setMaNV(String maNV) {
		this.maNV = maNV;
	}

	public String getMaKH() {
		return maKH;
	}

	public void setMaKH(String maKH) {
		this.maKH = maKH;
	}

	public String getMaKho() {
		return maKho;
	}

	public void setMaKho(String maKho) {
		this.maKho = maKho;
	}

}
